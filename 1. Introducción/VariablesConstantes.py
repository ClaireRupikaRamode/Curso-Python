'''Las variables se llaman variables porque su valor puede cambiar a lo largo del programa.'''

cantidadPersonas = 64

print(cantidadPersonas)

cantidadPersonas = 360

print(cantidadPersonas)

cantidadPersonas = 4769

print(cantidadPersonas)

'''Las constantes son valores que no cambian a lo largo del programa. Ya que no es como tal una regla del lenguaje de programación...
Por conveniencia, se escriben en mayúsculas para dar a entender que esa variable no se debe modificar de ninguna manera.'''

PI = 3.1416

print(PI)
