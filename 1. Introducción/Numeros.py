'''En python, los números se pueden clasificar en varios tipos principales: enteros (int), 
números de punto flotante (float) y números complejos (complex). 
A continuación, se presentan ejemplos de cada tipo de número y algunas operaciones básicas que se pueden realizar con ellos.'''

num1 = 64
num2 = 3.1416

print(type(num1))  # Salida: <class 'int'>
print(num1)        # Salida: 64

print(type(num2))  # Salida: <class 'float'>
print(num2)        # Salida: 3.1416

'''Ahora, podemos forzar la conversión de tipos entre enteros y flotantes.'''
'''Agregamos un salto de línea para mejorar la legibilidad de la salida.'''
print()  # Salto de línea
print(float(num1))  # Salida: 64.0
print(int(num2))    # Salida: 3
