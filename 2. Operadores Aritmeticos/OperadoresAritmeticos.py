'''En Python se puede utilizar los siguientes operadores aritméticos:'''

print(2 + 2) # Sumar
print(150 - 75) # Restar
print(10 * 10) # Multiplicar
print(75 / 9) # Dividir

'''Existen otros operadores aritméticos adicionales:'''

print(2 ** 3) # Potencia 2 elevado a 3
print(75 // 9) # División entera, solo toma la parte entera del resultado
print(100 % 100) # Módulo, devuelve el residuo de una división, no el cociente ni el dividendo
